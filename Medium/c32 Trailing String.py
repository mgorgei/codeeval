'''TRAILING STRING

There are two strings: A and B. Print 1 if string B occurs at the end of string
A. Otherwise, print 0.

INPUT SAMPLE:
The first argument is a path to the input filename containing two
comma-delimited strings, one per line. Ignore all empty lines in the input file.
For example:

Hello World,World
Hello CodeEval,CodeEval
San Francisco,San Jose

OUTPUT SAMPLE:
Print 1 if the second string occurs at the end of the first string. Otherwise,
print 0.
For example:

1
1
0
'''
def f(test='Hello World,World'):
    test = test.rstrip('\n').split(',')
    return '1' if test[0].endswith(test[1]) else '0'
